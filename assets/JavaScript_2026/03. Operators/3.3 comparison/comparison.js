
//JavaScript Comparison Operator
/*
    1.  ==      শুধুমাত্র মানের দিক দিয়ে সমান।
    2.  !=      শুধুমাত্র মানের দিক দিয়ে সমান নয়।
    3.  ===     ডাটা টাইপ ও মান উভয় দিক দিয়ে সমান।
    4.  !==     ডাটা টাইপ ও মান উভয় দিক দিয়ে সমান নয়।
    5.  >       বড়।
    6.  <       ছোট।
    7.  >=      বড অথবা সমান।
    8.  <=      ছোট অথবা সমান।
*/

    console.log(3 == 2);
    console.log(3 != 2);
